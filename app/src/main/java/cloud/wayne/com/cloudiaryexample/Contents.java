package cloud.wayne.com.cloudiaryexample;

public class Contents {
    String mName;
    String mUrl;

    public Contents() {
    }

    public String getName() {
        return mName;
    }

    public void setName(String name) {
        mName = name;
    }

    public String getUrl() {
        return mUrl;
    }

    public void setUrl(String url) {
        mUrl = url;
    }
}
